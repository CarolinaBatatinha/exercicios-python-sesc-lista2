# Faça um algoritmo para ler dois números é imprimir se eles são iguais, caso contrário, imprimir a mensagem: “os números são diferentes”.

numA = int(input('Digite um número inteiro: '))
numB = int(input('Digite um outro número inteiro: '))

if (numA == numB):
    print('Os números são iguais.')
else:
    print('Os números são diferentes.')
